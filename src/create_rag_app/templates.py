STREAMLIT_APP = dedent(
    """
    import os

    import requests
    import streamlit as st


    BACKEND_URL = os.getenv("STREAMLIT_BACKEND_URL", "http://localhost:8000")

    st.set_page_config(page_title="RAG Chat", page_icon=":books:")
    st.title("RAG Chat")
    st.caption("Thin Streamlit shell on top of the FastAPI backend")

    if "messages" not in st.session_state:
        st.session_state.messages = []

    try:
        health = requests.get(f"{BACKEND_URL}/health", timeout=5)
        if health.ok:
            st.success(f"Backend ready: {BACKEND_URL}")
        else:
            st.warning("Backend health check failed")
    except requests.RequestException:
        st.warning(f"Backend unavailable at {BACKEND_URL}")

    for message in st.session_state.messages:
        with st.chat_message(message["role"]):
            st.write(message["content"])
            for source in message.get("sources", []):
                with st.expander(source["path"]):
                    st.write(source["text"])

    prompt = st.chat_input("Ask the backend about your docs")
    if prompt:
        st.session_state.messages.append({"role": "user", "content": prompt, "sources": []})
        with st.chat_message("user"):
            st.write(prompt)

        try:
            response = requests.post(
                f"{BACKEND_URL}/chat",
                json={"message": prompt},
                timeout=60,
            )
            response.raise_for_status()
            payload = response.json()
            assistant_message = {
                "role": "assistant",
                "content": payload["answer"],
                "sources": payload["sources"],
            }
        except requests.RequestException as exc:
            assistant_message = {
                "role": "assistant",
                "content": f"Request failed: {exc}",
                "sources": [],
            }

        st.session_state.messages.append(assistant_message)
        with st.chat_message("assistant"):
            st.write(assistant_message["content"])
            for source in assistant_message["sources"]:
                with st.expander(source["path"]):
                    st.write(source["text"])
    """
).lstrip()
