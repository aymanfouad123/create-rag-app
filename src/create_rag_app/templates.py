def build_files(choices: InitChoices, cli_version: str) -> dict[str, str | dict]:
    files: dict[str, str | dict] = {}
    files[".gitignore"] = _gitignore()
    files["README.md"] = _root_readme(choices)
    files["rag.toml"] = _rag_toml(choices)
    files["create-rag-app.manifest.json"] = _manifest(choices, cli_version)
    if choices.include_sample_assets:
        files[".env.example"] = _env_example(choices.frontend)
        files["docs/sample.txt"] = SAMPLE_DOC
    files.update(_backend_files(choices))
    if choices.frontend == "nextjs":
        files.update(_nextjs_files())
    elif choices.frontend == "streamlit":
        files.update(_streamlit_files())
    return files


