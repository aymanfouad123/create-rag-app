NEXT_API = dedent(
    """
    import { ChatResponse } from "./types";

    const API_BASE_URL =
      process.env.NEXT_PUBLIC_API_BASE_URL ?? "http://localhost:8000";

    export async function sendMessage(message: string): Promise<ChatResponse> {
      const response = await fetch(`${API_BASE_URL}/chat`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ message }),
      });

      if (!response.ok) {
        throw new Error("Backend request failed");
      }

      return response.json();
    }
    """
).lstrip()


NEXT_CHAT_SHELL = dedent(
    """
    "use client";

    import { FormEvent, useState } from "react";

    import { sendMessage } from "../lib/api";
    import { Composer } from "./composer";
    import { MessageList } from "./message-list";
    import { ChatMessage } from "../lib/types";

    const initialMessage: ChatMessage = {
      role: "assistant",
      content: "Ask about the sample docs after you ingest them.",
      sources: [],
    };

    export function ChatShell() {
      const [messages, setMessages] = useState<ChatMessage[]>([initialMessage]);
      const [value, setValue] = useState("");
      const [loading, setLoading] = useState(false);
      const [status, setStatus] = useState("Backend: http://localhost:8000");

      async function onSubmit(event: FormEvent<HTMLFormElement>) {
        event.preventDefault();
        const message = value.trim();
        if (!message || loading) {
          return;
        }

        setLoading(true);
        setValue("");
        setMessages((current) => [...current, { role: "user", content: message, sources: [] }]);

        try {
          const response = await sendMessage(message);
          setMessages((current) => [
            ...current,
            {
              role: "assistant",
              content: response.answer,
              sources: response.sources,
            },
          ]);
          setStatus(`Retrieved ${response.sources.length} source chunks`);
        } catch (error) {
          const content =
            error instanceof Error ? error.message : "Unknown backend error";
          setMessages((current) => [
            ...current,
            { role: "assistant", content, sources: [] },
          ]);
          setStatus("Request failed");
        } finally {
          setLoading(false);
        }
      }

      return (
        <main className="shell">
          <section className="hero">
            <h1>Readable RAG.</h1>
            <p>
              This frontend is a thin shell. Retrieval, ingestion, and answer generation all stay in FastAPI.
            </p>
            <p className="status">{status}</p>
          </section>
          <section className="card">
            <MessageList messages={messages} loading={loading} />
            <Composer value={value} onChange={setValue} onSubmit={onSubmit} loading={loading} />
          </section>
        </main>
      );
    }
    """
).lstrip()


