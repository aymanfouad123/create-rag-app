INGEST_SERVICE = dedent(
    """
    from app.core.config import get_settings
    from app.rag.chunking import RecursiveChunker
    from app.rag.document_loader import load_local_documents
    from app.rag.embeddings import OpenAIEmbeddingClient
    from app.rag.vector_store import ChromaVectorStore
    from app.schemas.ingest import IngestResponse


    def ingest_documents() -> IngestResponse:
        settings = get_settings()
        documents = load_local_documents(settings.docs_path)
        chunker = RecursiveChunker(settings.chunk_size, settings.chunk_overlap)
        chunks = chunker.split_documents(documents)
        embedder = OpenAIEmbeddingClient(
            api_key=settings.openai_api_key,
            model=settings.embedding_model,
        )
        store = ChromaVectorStore(settings.vector_store_path, settings.vector_collection)
        store.upsert(chunks, embedder.embed_many([chunk["text"] for chunk in chunks]))
        return IngestResponse(
            files_indexed=len(documents),
            chunks_indexed=len(chunks),
            collection=settings.vector_collection,
        )
    """
).lstrip()


